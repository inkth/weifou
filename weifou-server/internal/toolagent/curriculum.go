// tiersFor 取某课程的档位标签与顺序：learn-logic 六幕、daodejing-full 九幕各有专属幕名，其余课程走默认三档。
func tiersFor(agentSlug string) (map[int]string, []int) {
	switch agentSlug {
	case "spoken-english":
		return englishTierLabels, englishTierOrder
	case "learn-logic":
		return logicTierLabels, logicTierOrder
	case "daodejing-full":
		return daodejingFullTierLabels, daodejingFullTierOrder
	case "learn-lifedesign":
		return lifedesignTierLabels, lifedesignTierOrder
	case "learn-love":
		return loveTierLabels, loveTierOrder
	case "learn-happiness":
		return happinessTierLabels, happinessTierOrder
	case "learn-writing":
		return writingTierLabels, writingTierOrder
	case "learn-learning":
		return learningTierLabels, learningTierOrder
	case "learn-negotiation":
		return negotiationTierLabels, negotiationTierOrder
	case "learn-habits":
		return habitsTierLabels, habitsTierOrder
	case "learn-business":
		return businessTierLabels, businessTierOrder
	case "learn-dating":
		return datingTierLabels, datingTierOrder
	case "learn-meditation":
		return meditationTierLabels, meditationTierOrder
	}
	return tierLabels, tierOrder
}

